package section02;

public class Var8 {
    public static void main(String[] args) {
        // 1. 정수형
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32768 ~ 32767
        int i = 2147483647; // -2147483648 ~ 2147483647
        long l = 9223372036854775807L; // -9223372036854775808 ~ 9223372036854775807

        // 2. 실수형
        float f = 3.14f;
        double d = 3.14;
    }
}
